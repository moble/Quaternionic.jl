@doc raw"""
    Lorentz{T<:Real}

A proper orthochronous Lorentz transformation, an element of Spin⁺(3,1) — the double cover
of the restricted Lorentz group SO⁺(3,1).

We work in the spacetime algebra ``Cl(3,1)`` with metric signature ``{−}{+}{+}{+}`` and
basis vectors `𝐭, 𝐱, 𝐲, 𝐳` satisfying `𝐭² = −1`, `𝐱² = 𝐲² = 𝐳² = +1`, all mutually
anticommuting.  The pseudoscalar ``𝐈 = 𝐭𝐱𝐲𝐳``` satisfies ``𝐈² = −𝟏``.

Elements of ``𝐑 ∈ \mathrm{Spin}⁺(3,1)`` live in the even subalgebra (grades 0, 2, 4) and
satisfy ``𝐑 𝐑̃ = 𝟏``, where ``𝐑̃`` is the GA reverse.  In particular, note that that
product *could* include a term proportional to the pseudoscalar; it is zero: ``𝐑 𝐑̃ = 𝟏 +
0 𝐈``.  This subalgebra splits into two parts:

- **Rotation subgroup**: generated by the spatial bivectors `𝐲𝐳`, `𝐱𝐳`, `𝐱𝐲`.
- **Boost sector**: generated by the timelike bivectors `𝐭𝐱`, `𝐭𝐲`, `𝐭𝐳`.

All elements of this algebra commute with the pseudoscalar ``𝐈``, which acts as the complex
unit for this subalgebra.  Note that ``𝐈·𝐲𝐳 = −𝐭𝐱``, ``𝐈·𝐱𝐳 = +𝐭𝐲``, ``𝐈·𝐱𝐲 =
−𝐭𝐳``, relating spatial-rotation generators to boost generators.  A boost along ``𝐧 =
(nˣ, nʸ, nᶻ)`` with rapidity ``η`` is thus

```math
𝐑 = \cosh(η/2)·𝟏 + \sinh(η/2)·(nˣ 𝐭𝐱 + nʸ 𝐭𝐲 + nᶻ 𝐭𝐳)
```

The two elements ``±R ∈ \mathrm{Spin}⁺(3,1)`` represent the same Lorentz transformation.
The real GA components and their encoding are described in
[`components(::Lorentz)`](@ref).

## Operations

Compose with `*`, invert with `inv`, obtain the identity with `one`.
Apply to a Minkowski 4-vector `v = [vᵗ, vˣ, vʸ, vᶻ]` by calling `Λ(v)`.
Access the raw GA components via [`components`](@ref) and the
internal rotor via [`rotor`](@ref).

## Constructors

Use the named constructors [`Rotation`](@ref) and [`Boost`](@ref).
"""
struct Lorentz{T<:Real}
    rotor::Quaternion{Complex{T}}
end

# ---------------------------------------------------------------------------
# Accessor
# ---------------------------------------------------------------------------

"""
    rotor(Λ::Lorentz{T}) → Quaternion{Complex{T}}

Return the internal Spin⁺(3,1) rotor of `Λ` as a biquaternion (quaternion with
complex coefficients).
"""
rotor(Λ::Lorentz) = Λ.rotor

@doc raw"""
    components(Λ::Lorentz{T}) → SVector{8, T}

Return the eight real geometric-algebra components of `Λ` in Cl(3,1), ordered
as in the even-subalgebra expansion of a Spin⁺(3,1) rotor:

```math
𝐑 = R¹ + Rᵗˣ 𝐭𝐱 + Rᵗʸ 𝐭𝐲 + Rᵗᶻ 𝐭𝐳 + Rˣʸ 𝐱𝐲 + Rˣᶻ 𝐱𝐳 + Rʸᶻ 𝐲𝐳 + Rᵗˣʸᶻ 𝐈
```

where ``𝐈 = 𝐭𝐱𝐲𝐳`` is the pseudoscalar of ``\mathrm{Cl}(3,1)``.

## Storage mapping

`Lorentz{T}` stores a unit quaternion ``𝐑 = w𝟏 + x𝐢 + y𝐣 + z𝐤`` with complex
coefficients ``w, x, y, z ∈ ℂ``, where the quaternionic basis elements `𝐢, 𝐣, 𝐤`
correspond to spatial bivectors according to
```math
\begin{gather}
𝐢 = 𝐳𝐲 = -𝐲𝐳, \\
𝐣 = 𝐱𝐳 = -𝐳𝐱, \\
𝐤 = 𝐲𝐱 = -𝐱𝐲,
\end{gather}
```
and the complex unit ``𝒾 ∈ ℂ`` maps to the pseudoscalar ``𝐈 = 𝐭𝐱𝐲𝐳``, so that
```math
\begin{gather}
𝒾𝐢 = 𝐭𝐱𝐲𝐳𝐳𝐲 = 𝐭𝐱, \\
𝒾𝐣 = 𝐭𝐱𝐲𝐳𝐱𝐳 = 𝐭𝐲, \\
𝒾𝐤 = 𝐭𝐱𝐲𝐳𝐲𝐱 = 𝐭𝐳,
\end{gather}
```

The eight real GA components read off from `w, x, y, z` as:

| GA component | quaternion expression | Cl(3,1) basis |
| :----------- | :-------------------- | :------------ |
| `R¹`         | ``\Re(w)``            | ``𝟏``         |
| `Rᵗˣ`        | ``\Im(x)``            | ``𝐭𝐱``        |
| `Rᵗʸ`        | ``\Im(y)``            | ``𝐭𝐲``        |
| `Rᵗᶻ`        | ``\Im(z)``            | ``𝐭𝐳``        |
| `Rˣʸ`        | ``-\Re(z)``           | ``𝐱𝐲``        |
| `Rˣᶻ`        | ``\Re(y)``            | ``𝐱𝐳``        |
| `Rʸᶻ`        | ``-\Re(x)``           | ``𝐲𝐳``        |
| `Rᵗˣʸᶻ`      | ``\Im(w)``            | ``𝐭𝐱𝐲𝐳``      |

The unit-norm condition ``𝐑𝐑̃ = 𝟏`` — i.e., ``w² + x² + y² + z² = 1`` with complex
arithmetic — translates to two real conditions on the GA components:

```math
\begin{gather}
(R¹)² + (Rˣʸ)² + (Rˣᶻ)² + (Rʸᶻ)² - (Rᵗˣ)² - (Rᵗʸ)² - (Rᵗᶻ)² - (Rᵗˣʸᶻ)² = 1, \\
R¹ Rᵗˣʸᶻ - Rʸᶻ Rᵗˣ + Rˣᶻ Rᵗʸ - Rˣʸ Rᵗᶻ = 0.
\end{gather}
```
"""
function components(Λ::Lorentz{T}) where {T<:Real}
    w, x, y, z = components(rotor(Λ))
    @SVector [real(w), imag(x), imag(y), imag(z), -real(z), real(y), -real(x), imag(w)]
end

# ---------------------------------------------------------------------------
# Named constructors
# ---------------------------------------------------------------------------

"""
    Rotation(R::Rotor{T}) → Lorentz{T}

Construct the `Lorentz{T}` element corresponding to the pure spatial rotation
encoded by the unit quaternion `R ∈ Spin(3)`.

The rotation subgroup embeds into Spin⁺(3,1) by extending the real quaternion
components to `Complex{T}` with zero imaginary parts.
"""
function Rotation(R::Rotor{T}) where {T<:Real}
    w, x, y, z = components(R)
    return Lorentz{T}(
        Quaternion(complex(w), complex(x), complex(y), complex(z))
    )
end

"""
    Boost(η::T, n̂::AbstractVector) → Lorentz{T}

Construct the pure boost with rapidity `η` along the unit 3-vector
`n̂ = [nˣ, nʸ, nᶻ]`.

In the even subalgebra of Cl(3,1) the rotor is

```math
𝐑 = \\cosh(η/2)·𝟏 + \\sinh(η/2)·(nˣ\\,𝐭𝐱 + nʸ\\,𝐭𝐲 + nᶻ\\,𝐭𝐳).
```

See [`components(::Lorentz)`](@ref) for the correspondence between
this GA form and the quaternion storage.
"""
function Boost(η::T, n̂::AbstractVector) where {T<:Real}
    ch, sh = cosh(η / 2), sinh(η / 2)
    return Lorentz{T}(
        Quaternion(
            complex(ch),
            complex(zero(T), sh * T(n̂[1])),
            complex(zero(T), sh * T(n̂[2])),
            complex(zero(T), sh * T(n̂[3])),
        ),
    )
end

# ---------------------------------------------------------------------------
# Group operations
# ---------------------------------------------------------------------------

"""Compose two Lorentz transformations: `(Λ₁ * Λ₂)(v) == Λ₁(Λ₂(v))`."""
function Base.:*(Λ₁::Lorentz{T}, Λ₂::Lorentz{T}) where {T<:Real}
    return Lorentz{T}(rotor(Λ₁) * rotor(Λ₂))
end

"""
Group inverse.  For a unit biquaternion `R·R̃ = 1`, so `R⁻¹ = R̃ = conj(R)`
(the GA reverse equals the quaternionic conjugate).
"""
Base.inv(Λ::Lorentz{T}) where {T<:Real} = Lorentz{T}(conj(rotor(Λ)))

"""Identity element of `Lorentz{T}`."""
Base.one(::Type{Lorentz{T}}) where {T<:Real} =
    Lorentz{T}(one(Quaternion{Complex{T}}))
Base.one(Λ::Lorentz) = one(typeof(Λ))

# ---------------------------------------------------------------------------
# Action on Minkowski 4-vectors
# ---------------------------------------------------------------------------

"""
    (Λ::Lorentz{T})(v::AbstractVector) → similar(v, T)

Apply `Λ` to the Minkowski 4-vector `v = [vᵗ, vˣ, vʸ, vᶻ]` (signature −+++)
and return the transformed vector in a fresh container of the same type with
element type `T`.

The action is the Spin⁺(3,1) sandwich `V′ = R·V·R̃` in Cl(3,1), where `R̃` is
the GA reverse.  The eight real GA components `(R¹, Rᵗˣ, …, Rᵗˣʸᶻ)` are
extracted via [`components(::Lorentz)`](@ref), and the bilinear
expansion of the grade-1 projection of `R·V·R̃` is applied directly.
"""
function (Λ::Lorentz{T})(v::AbstractVector) where {T<:Real}
    R¹, Rᵗˣ, Rᵗʸ, Rᵗᶻ, Rˣʸ, Rˣᶻ, Rʸᶻ, Rᵗˣʸᶻ = components(Λ)
    vᵗ = T(v[1])
    vˣ = T(v[2])
    vʸ = T(v[3])
    vᶻ = T(v[4])

    v′ᵗ =
        vᵗ * (R¹^2 + Rᵗˣ^2 + Rᵗˣʸᶻ^2 + Rᵗʸ^2 + Rᵗᶻ^2 + Rˣʸ^2 + Rˣᶻ^2 + Rʸᶻ^2) +
        vˣ * (2R¹ * Rᵗˣ + 2Rᵗˣʸᶻ * Rʸᶻ - 2Rᵗʸ * Rˣʸ - 2Rᵗᶻ * Rˣᶻ) +
        vʸ * (2R¹ * Rᵗʸ + 2Rᵗˣ * Rˣʸ - 2Rᵗˣʸᶻ * Rˣᶻ - 2Rᵗᶻ * Rʸᶻ) +
        vᶻ * (2R¹ * Rᵗᶻ + 2Rᵗˣ * Rˣᶻ + 2Rᵗˣʸᶻ * Rˣʸ + 2Rᵗʸ * Rʸᶻ)

    v′ˣ =
        vᵗ * (2R¹ * Rᵗˣ + 2Rᵗˣʸᶻ * Rʸᶻ + 2Rᵗʸ * Rˣʸ + 2Rᵗᶻ * Rˣᶻ) +
        vˣ * (R¹^2 + Rᵗˣ^2 + Rᵗˣʸᶻ^2 - Rᵗʸ^2 - Rᵗᶻ^2 - Rˣʸ^2 - Rˣᶻ^2 + Rʸᶻ^2) +
        vʸ * (2R¹ * Rˣʸ + 2Rᵗˣ * Rᵗʸ - 2Rᵗˣʸᶻ * Rᵗᶻ - 2Rˣᶻ * Rʸᶻ) +
        vᶻ * (2R¹ * Rˣᶻ + 2Rᵗˣ * Rᵗᶻ + 2Rᵗˣʸᶻ * Rᵗʸ + 2Rˣʸ * Rʸᶻ)

    v′ʸ =
        vᵗ * (2R¹ * Rᵗʸ - 2Rᵗˣ * Rˣʸ - 2Rᵗˣʸᶻ * Rˣᶻ + 2Rᵗᶻ * Rʸᶻ) +
        vˣ * (-2R¹ * Rˣʸ + 2Rᵗˣ * Rᵗʸ + 2Rᵗˣʸᶻ * Rᵗᶻ - 2Rˣᶻ * Rʸᶻ) +
        vʸ * (R¹^2 - Rᵗˣ^2 + Rᵗˣʸᶻ^2 + Rᵗʸ^2 - Rᵗᶻ^2 - Rˣʸ^2 + Rˣᶻ^2 - Rʸᶻ^2) +
        vᶻ * (2R¹ * Rʸᶻ - 2Rᵗˣ * Rᵗˣʸᶻ + 2Rᵗʸ * Rᵗᶻ - 2Rˣʸ * Rˣᶻ)

    v′ᶻ =
        vᵗ * (2R¹ * Rᵗᶻ - 2Rᵗˣ * Rˣᶻ + 2Rᵗˣʸᶻ * Rˣʸ - 2Rᵗʸ * Rʸᶻ) +
        vˣ * (-2R¹ * Rˣᶻ + 2Rᵗˣ * Rᵗᶻ - 2Rᵗˣʸᶻ * Rᵗʸ + 2Rˣʸ * Rʸᶻ) +
        vʸ * (-2R¹ * Rʸᶻ + 2Rᵗˣ * Rᵗˣʸᶻ + 2Rᵗʸ * Rᵗᶻ - 2Rˣʸ * Rˣᶻ) +
        vᶻ * (R¹^2 - Rᵗˣ^2 + Rᵗˣʸᶻ^2 - Rᵗʸ^2 + Rᵗᶻ^2 + Rˣʸ^2 - Rˣᶻ^2 - Rʸᶻ^2)

    vout = similar(v, T)
    vout[1] = v′ᵗ
    vout[2] = v′ˣ
    vout[3] = v′ʸ
    vout[4] = v′ᶻ
    return vout
end
